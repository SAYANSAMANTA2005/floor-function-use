#include <iostream>
#include <cmath>

int main() {
    double num1 = 4.9;
    double num2 = 7.2;
    double num3 = -3.7;

    std::cout << "Floor of " << num1 << " is: " << floor(num1) << std::endl;
    std::cout << "Floor of " << num2 << " is: " << floor(num2) << std::endl;
    std::cout << "Floor of " << num3 << " is: " << floor(num3) << std::endl;

    return 0;
}